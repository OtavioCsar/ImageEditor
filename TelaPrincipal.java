    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
        JSlider slider = (JSlider) evt.getSource();
        int zoomValue = slider.getValue();

        try {
            BufferedImage imagem = ImageIO.read(arquivoSelecionado);

            int width = (int) (imagem.getWidth() * (zoomValue / 50.0));
            int height = (int) (imagem.getHeight() * (zoomValue / 50.0));

            Image scaledImage = imagem.getScaledInstance(width, height, Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(scaledImage));
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jSlider1StateChanged

