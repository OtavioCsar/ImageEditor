    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            BufferedImage imagem = ImageIO.read(arquivoSelecionado);
            var novaLargura = Integer.valueOf(jTextField1.getText());
            int larguraOriginal = imagem.getWidth();
            int alturaOriginal = imagem.getHeight();
            double proporcao = (double) alturaOriginal / larguraOriginal;
            int novaAltura = (int) (proporcao * novaLargura);
            BufferedImage novaImagem = new BufferedImage(novaLargura, novaAltura, BufferedImage.TYPE_INT_RGB);
            Graphics2D gd = novaImagem.createGraphics();
            gd.drawImage(imagem, 0, 0, novaLargura, novaAltura, null);
            gd.dispose();
            jLabel1.setIcon(new ImageIcon(novaImagem));
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

